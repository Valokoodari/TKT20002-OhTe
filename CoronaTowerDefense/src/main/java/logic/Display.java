package logic;

public class Display {
	public int width;
	public int height;
	
	/**
	 * Luo olion säilyttämään tietoja nykyisestä näytöstä
	 * 
	 * @param width  Näytön leveys pikseleissä
	 * @param height  Näytön korkeus pikseleissä
	 */
	public Display(int width, int height) {
		this.width = width;
		this.height = height;
	}
}
